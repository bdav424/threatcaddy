let patched = 0;
let skipped = 0;

for (const [lang, newKeys] of Object.entries(translations)) {
  const filePath = path.join(LOCALES_DIR, lang, 'settings.json');
  if (!fs.existsSync(filePath)) {
    console.log(`SKIP ${lang} — file not found`);
    skipped++;
    continue;
  }

  const existing = JSON.parse(fs.readFileSync(filePath, 'utf8'));
  let added = 0;

  for (const [key, value] of Object.entries(newKeys)) {
    if (!(key in existing)) {
      existing[key] = value;
      added++;
    }
  }

  if (added > 0) {
    fs.writeFileSync(filePath, JSON.stringify(existing, null, 2) + '\n', 'utf8');
    console.log(`PATCHED ${lang}: +${added} keys`);
    patched++;
  } else {
    console.log(`SKIP ${lang}: all keys already present`);
    skipped++;
  }
}

console.log(`\nDone. Patched: ${patched}, Skipped: ${skipped}`);
